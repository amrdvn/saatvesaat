  data() {
    return {
      id: this.$route.params.id,
      Seppet: [],
      user: '',
      products: [],
      editadet: '',
    }
  },

  mounted() {
    if (this.user != null) {
      fbDb
        .ref('Sepet/8Mt43rDDxdZHhN0ufZbxM8Z1rrb2')
        .get()
        .then((snapshot) => {
          if (snapshot.exists()) {
            this.Seppet = snapshot.val()
          }
        })
    }

    firebase.auth().onAuthStateChanged((user) => {
      console.log(user)
      this.user = user
    })

    fbDb
      .ref('products')
      .get()
      .then((snapshot) => {
        if (snapshot.exists()) {
          this.products = snapshot.val()
        }
      })
  },

  methods: {
    
    sepettenCikar(sepet) {
      const spt = this.Seppet
      const productIndex = spt.findIndex((item) => item.code === sepet.code)
      spt.splice(productIndex, 1)
      fbDb.ref('Sepet/' + this.user.uid).set(spt)
    },
    urunartibir(sepet) {
      const spt = this.Seppet
      const productIndex = spt.findIndex((item) => item.code === sepet.code)
      spt[productIndex].adet += 1

      fbDb.ref('Sepet/' + this.user.uid).set(spt)
    },
    sepeteEkle(product) {
      
      let exists=false;
      for(const item of this.sepet)
      {
        if(item.id===product.id){
          item.adet=item.adet+1;
          exists=true;
          break;
        }
        
      }
         

      if(!exists){
        this.sepet.push({
          ...product,
          adet:1,
          
        })
        fbDb.ref('Sepet/'+ this.user.uid).update(this.sepet)
      }
      
    },
  },
  
  // eslint-disable-next-line vue/order-in-components
  computed: {
    toplamFiyat() {
      let toplam = 0
      for (const product of this.Seppet) {
        toplam += product.fiyat * product.adet
      }
      return toplam
    },
    sepettekiUrunSayisi() {
      let sepetUrunAdet = 0
      for (const product of this.Seppet) {
        sepetUrunAdet += product.adet
      }
      return sepetUrunAdet
    },
    post(product) {
    
      const spt=this.products
      const productIndex=spt.find(item =>item.id===product.id)
      const productIndex=spt.findIndex(item =>item.code===sepet.code)
      spt[productIndex].adet-=1
    },
  },
}
